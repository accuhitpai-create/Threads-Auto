def get_prompt(pillar: str, content_type: str, title: str, notes: str = "") -> str:
    pillar_templates = PILLAR_TEMPLATES.get(pillar, {})
    template = pillar_templates.get(content_type)

    if not template:
        # 找不到對應模板，用通用模板
        template = """【主題】{title}
【任務】依照「AI 與數據大師」的定位，寫一篇 Threads 貼文分享相關知識。

主文結構（150–250 字，要短要精）：
- 鉤子：吸引目標讀者的開場
- 核心內容：3–5 個重點（條列）
- 條列寫完就結束，不要加開放提問

留言2：
「歡迎來聊聊更多 👉 {link}」"""

    user_prompt = template.format(title=title, link=CONSULTATION_LINK)

    if notes:
        user_prompt += f"\n\n【修改備註】\n請根據以下意見調整：{notes}"

    return user_prompt
